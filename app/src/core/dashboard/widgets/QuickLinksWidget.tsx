export default function QuickLinksWidget({ widgetId }: { widgetId: string; size: string }) {
  const { t } = useTranslation('dashboard')
  const { can } = usePermission()
  const { isActive } = useFeature()

  const isAdmin = widgetId === 'quick_links_admin'
  const links = isAdmin ? ADMIN_LINKS : USER_LINKS

  const visibleLinks = useMemo(
    () => links.filter(l =>
      (!l.permission || can(l.permission)) &&
      (!l.featureGate || isActive(l.featureGate))
    ),
    [links, can, isActive]
  )

  if (visibleLinks.length === 0) return null

  return (
    <div className="dashboard-quick-links">
      <h3 className="home-section-title">
        <svg className="home-section-icon" width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" strokeWidth="2" strokeLinecap="round" strokeLinejoin="round" aria-hidden="true">
          {isAdmin ? (
            <>
              <circle cx="12" cy="12" r="3" />
              <path d="M19.4 15a1.65 1.65 0 0 0 .33 1.82l.06.06a2 2 0 0 1 0 2.83 2 2 0 0 1-2.83 0l-.06-.06a1.65 1.65 0 0 0-1.82-.33 1.65 1.65 0 0 0-1 1.51V21a2 2 0 0 1-2 2 2 2 0 0 1-2-2v-.09A1.65 1.65 0 0 0 9 19.4a1.65 1.65 0 0 0-1.82.33l-.06.06a2 2 0 0 1-2.83 0 2 2 0 0 1 0-2.83l.06-.06A1.65 1.65 0 0 0 4.68 15a1.65 1.65 0 0 0-1.51-1H3a2 2 0 0 1-2-2 2 2 0 0 1 2-2h.09" />
            </>
          ) : (
            <>
              <path d="M3 9l9-7 9 7v11a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2z" />
              <polyline points="9 22 9 12 15 12 15 22" />
            </>
          )}
        </svg>
        {isAdmin ? t('admin_links_title') : t('user_links_title')}
      </h3>
      <nav className="auto-grid-sm" aria-label={isAdmin ? t('admin_links_title') : t('user_links_title')}>
        {visibleLinks.map(link => (
          <Link key={link.path + link.labelKey} to={link.path} className="card-link-item card-hover-lift">
            <div className="unified-card">
              <div className="card-link-item-content">
                {link.icon}
                <span className="card-link-item-label">{t(link.labelKey)}</span>
              </div>
            </div>
          </Link>
        ))}
      </nav>
    </div>
  )
}
